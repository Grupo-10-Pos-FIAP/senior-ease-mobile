class UserProfile {
  const UserProfile({
    required this.fullName,
    required this.age,
    required this.registration,
    required this.disabilityDescription,
    required this.email,
    required this.phone,
  });

  final String fullName;
  final String age;
  final String registration;
  final String disabilityDescription;
  final String email;
  final String phone;
}
